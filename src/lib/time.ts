export const DAY_TIME = 24 * 60 * 60 * 1000;
export const MONTH_TIME = 30 * DAY_TIME;
export const YEAR_TIME = 12 * MONTH_TIME;
export const MILLIONAIRE_TIME = 100 * YEAR_TIME;

// FIRE refers to Financial Independence Retire Early. It is considered that you've reached
// FI when you can sustain a 4% withdrawal rate for your yearly expenses; which roughly
// translates to having savings for 26 years of expenses.
//
// See https://en.wikipedia.org/wiki/FIRE_movement
export const FIRE_TIME = 26 * YEAR_TIME;
